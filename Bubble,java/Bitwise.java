public class Bitwise {
    public static void main(String[] args) {
        //OR
        System.out.println(5|6);
        //AND
        System.out.println(5&6);
        //XOR
        System.out.println(5^6);
        //~
        System.out.println(~5);
        //NOT
        System.out.println(~0);
    }
    
}
